"""Agent context pack generation."""

from __future__ import annotations

import json
from pathlib import Path

from omnivorous.agents import AgentTarget, resolve_agents
from omnivorous.chunker import chunk_markdown, write_chunks
from omnivorous.frontmatter import add_frontmatter
from omnivorous.models import ConvertResult, DocumentMetadata
from omnivorous.registry import ensure_registry_loaded, get_converter, supported_extensions


def generate_agent_instructions(
    docs_metadata: list[DocumentMetadata], agent: AgentTarget
) -> str:
    """Generate an instruction file for the given agent target."""
    lines = [
        "# Project Context",
        "",
        "This directory contains documentation converted by omnivorous for use as AI agent context.",
        "",
        "## Available Documents",
        "",
    ]
    for meta in docs_metadata:
        tokens = f"~{meta.tokens_estimate} tokens" if meta.tokens_estimate else ""
        label = f"- **{meta.title}** ({meta.format}, {tokens})" if tokens else f"- **{meta.title}** ({meta.format})"
        lines.append(label)

    lines.extend([
        "",
        "## Usage",
        "",
        "Read the documents in `docs/` for project context. "
        "See `manifest.json` for a full listing.",
        "",
        f"Generated by omnivorous for {agent.display_name}.",
    ])
    return "\n".join(lines) + "\n"


def generate_claude_md(docs_metadata: list[DocumentMetadata]) -> str:
    """Generate a CLAUDE.md file with agent instructions. Kept for backward compatibility."""
    from omnivorous.agents import AGENT_TARGETS

    return generate_agent_instructions(docs_metadata, AGENT_TARGETS["claude"])


def generate_project_context(docs_metadata: list[DocumentMetadata]) -> str:
    """Generate a PROJECT_CONTEXT.md summary."""
    lines = [
        "# Project Documentation Summary",
        "",
        f"Total documents: {len(docs_metadata)}",
        f"Total estimated tokens: {sum(m.tokens_estimate for m in docs_metadata)}",
        "",
        "## Documents",
        "",
    ]
    for meta in docs_metadata:
        lines.append(f"### {meta.title}")
        lines.append(f"- Source: `{meta.source}`")
        lines.append(f"- Format: {meta.format}")
        if meta.pages:
            lines.append(f"- Pages: {meta.pages}")
        if meta.headings:
            lines.append(f"- Headings: {len(meta.headings)}")
        if meta.tables:
            lines.append(f"- Tables: {meta.tables}")
        if meta.images:
            lines.append(f"- Images: {meta.images}")
        lines.append(f"- Tokens: {meta.tokens_estimate}")
        lines.append("")

    return "\n".join(lines)


def generate_manifest(
    docs_metadata: list[DocumentMetadata], output_files: list[str]
) -> str:
    """Generate a manifest.json for the agent context pack."""
    manifest = {
        "version": "1.0",
        "generator": "omnivorous",
        "documents": [m.to_dict() for m in docs_metadata],
        "output_files": output_files,
        "total_tokens": sum(m.tokens_estimate for m in docs_metadata),
    }
    return json.dumps(manifest, indent=2) + "\n"


def resolve_output_paths(
    source_files: list[Path], source_dir: Path
) -> dict[Path, Path]:
    """Map each source file to a unique relative output path under the output directory.

    Preserves directory structure from source_dir. If multiple files in the same
    directory share the same stem, disambiguates by appending the original extension
    to the stem (e.g. ``readme.md`` + ``readme.txt`` → ``readme_md.md`` + ``readme_txt.md``).
    """
    from collections import defaultdict

    # Compute initial relative output paths (preserving directory structure)
    initial: dict[Path, Path] = {}
    for f in source_files:
        initial[f] = f.relative_to(source_dir).with_suffix(".md")

    # Group by output path to find collisions
    groups: dict[Path, list[Path]] = defaultdict(list)
    for src, out in initial.items():
        groups[out].append(src)

    resolved: dict[Path, Path] = {}
    for out_path, sources in groups.items():
        if len(sources) == 1:
            resolved[sources[0]] = out_path
        else:
            # Disambiguate by appending the original extension (without dot) to the stem
            for src in sources:
                ext_tag = src.suffix.lstrip(".")
                new_name = f"{out_path.stem}_{ext_tag}.md"
                resolved[src] = out_path.parent / new_name

    return resolved


def pack_context(
    source_dir: Path,
    output_dir: Path,
    chunk_size: int = 500,
    chunk_by: str = "heading",
    agents: list[str] | None = None,
) -> Path:
    """Orchestrate full pipeline: convert all docs, chunk, and generate agent context pack."""
    ensure_registry_loaded()
    exts = set(supported_extensions())
    docs_dir = output_dir / "docs"
    docs_dir.mkdir(parents=True, exist_ok=True)

    results: list[ConvertResult] = []
    all_output_files: list[str] = []

    # Find and convert all supported files
    source_files = sorted(
        f for f in source_dir.rglob("*") if f.is_file() and f.suffix.lower() in exts
    )

    output_map = resolve_output_paths(source_files, source_dir)

    for source_file in source_files:
        ext = source_file.suffix.lower()
        converter = get_converter(ext)
        result = converter.convert(source_file)
        results.append(result)

        # Add frontmatter and write converted markdown
        out_rel = output_map[source_file]
        md_with_frontmatter = add_frontmatter(result.content, result.metadata.to_dict())
        out_path = docs_dir / out_rel
        out_path.parent.mkdir(parents=True, exist_ok=True)
        out_path.write_text(md_with_frontmatter, encoding="utf-8")
        all_output_files.append(f"docs/{out_rel.as_posix()}")

        # Chunk if content is large enough
        chunk_result = chunk_markdown(result.content, result.metadata, chunk_by, chunk_size)
        if len(chunk_result.chunks) > 1:
            chunk_paths = write_chunks(
                chunk_result.chunks, out_rel.stem, out_path.parent
            )
            for cp in chunk_paths:
                chunk_rel = cp.relative_to(docs_dir)
                all_output_files.append(f"docs/{chunk_rel.as_posix()}")

    # Generate meta files
    all_metadata = [r.metadata for r in results]

    resolved = resolve_agents(agents or ["claude"])
    for agent in resolved:
        instructions = generate_agent_instructions(all_metadata, agent)
        agent_path = output_dir / agent.file_path
        agent_path.parent.mkdir(parents=True, exist_ok=True)
        agent_path.write_text(instructions, encoding="utf-8")
        all_output_files.append(agent.file_path)

    project_ctx = generate_project_context(all_metadata)
    (output_dir / "PROJECT_CONTEXT.md").write_text(project_ctx, encoding="utf-8")
    all_output_files.append("PROJECT_CONTEXT.md")

    all_output_files.append("manifest.json")
    manifest = generate_manifest(all_metadata, all_output_files)
    (output_dir / "manifest.json").write_text(manifest, encoding="utf-8")

    return output_dir
